#ifndef ENV_MONITOR_UAPI_H
#define ENV_MONITOR_UAPI_H

#ifdef __KERNEL__
#include <linux/ioctl.h>
#else
#include <sys/ioctl.h>
#endif

#define SSD1315_WIDTH 128
#define SSD1315_HEIGHT 64
#define SSD1315_PAGES (SSD1315_HEIGHT / 8)
#define SSD1315_FB_SIZE (SSD1315_WIDTH * SSD1315_PAGES)

#define SSD1315_IOC_MAGIC 'o'
#define SSD1315_IOC_CLEAR _IO(SSD1315_IOC_MAGIC, 0x01)
#define SSD1315_IOC_DISPLAY_ON _IO(SSD1315_IOC_MAGIC, 0x02)
#define SSD1315_IOC_DISPLAY_OFF _IO(SSD1315_IOC_MAGIC, 0x03)
#define SSD1315_IOC_INVERT_ON _IO(SSD1315_IOC_MAGIC, 0x04)
#define SSD1315_IOC_INVERT_OFF _IO(SSD1315_IOC_MAGIC, 0x05)

#endif
